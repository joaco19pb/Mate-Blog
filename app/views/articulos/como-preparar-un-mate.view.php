      <h2>Guía Paso a Paso: Cómo Preparar un Mate Perfecto</h2>

      <p>
        Preparar un buen mate es un arte que se perfecciona con la práctica.
        Aunque hay variaciones regionales, aquí te presentamos una guía general
        para cebar un mate tradicional y disfrutar de una experiencia auténtica.
      </p>

      <h3>Ingredientes y Utensilios:</h3>
      <ul>
        <li>Mate (recipiente).</li>
        <li>Bombilla.</li>
        <li>Yerba mate de tu preferencia.</li>
        <li>Agua caliente (idealmente entre 70-80°C, nunca hirviendo).</li>
        <li>Termo (para mantener la temperatura del agua).</li>
      </ul>

      <h3>Pasos para la Preparación:</h3>
      <ol>
        <li>
          <strong>Llenar el Mate con Yerba:</strong> Llena el mate con yerba
          hasta aproximadamente dos tercios o tres cuartas partes de su
          capacidad. No lo llenes por completo.
        </li>
        <li>
          <strong>Formar la "Montañita":</strong> Tapa la boca del mate con la
          palma de tu mano, inviértelo y agítalo suavemente. Esto ayuda a que el
          polvo (componente más fino de la yerba) quede en la parte superior y
          no obstruya la bombilla. Al volver el mate a su posición normal, la
          yerba deberá quedar inclinada, formando una especie de "montañita" o
          pendiente en un lado.
        </li>
        <li>
          <strong>Humedecer la Yerba:</strong> Vierte un poco de agua tibia (o a
          la temperatura que usarás para cebar) sobre la parte más baja de la
          yerba inclinada, donde hay menos cantidad. Deja que la yerba absorba
          esta agua por unos segundos. Esto ayuda a que la yerba se hinche y
          evita que se queme con el primer chorro de agua caliente.
        </li>
        <li>
          <strong>Introducir la Bombilla:</strong> Inserta la bombilla en la
          zona humedecida, presionándola suavemente contra la pared interna del
          mate, hasta que llegue al fondo. Una vez colocada, ¡no la muevas más!
          Mover la bombilla puede hacer que se tape.
        </li>
        <li>
          <strong>Cebar el Mate:</strong> Comienza a cebar vertiendo agua
          caliente (70-80°C) lentamente cerca de la bombilla, en el hueco que se
          formó al humedecer. No mojes toda la yerba de golpe; la idea es ir
          mojando la "montañita" gradualmente con cada cebada. Esto permite que
          el mate rinda más y mantenga su sabor por más tiempo.
        </li>
        <li>
          <strong>Disfrutar y Compartir:</strong> El primer mate suele ser el
          más amargo y a veces lo toma quien lo prepara. Luego, se sigue cebando
          y pasando el mate en ronda, usualmente en el sentido de las agujas del
          reloj. Cada persona toma el mate completo hasta escuchar un "ronquido"
          (ruido de la bombilla al terminarse el agua) y lo devuelve al cebador.
        </li>
      </ol>

      <h3>Consejos para un Mate Perfecto:</h3>
      <ul>
        <li>
          <strong>Temperatura del Agua:</strong> Es crucial. Agua demasiado fría
          no extraerá bien el sabor, y agua hirviendo "quema" la yerba, dándole
          un sabor amargo y lavándola rápidamente. Usa un termómetro o espera a
          que el agua esté caliente pero no burbujeando (justo antes del punto
          de ebullición).
        </li>
        <li>
          <strong>No Inundar:</strong> Ceba siempre en el mismo lugar,
          manteniendo seca la parte superior de la "montañita" de yerba el mayor
          tiempo posible.
        </li>
        <li>
          <strong>Yerba de Calidad:</strong> La elección de la yerba influye
          mucho en el sabor y la duración del mate. Experimenta con diferentes
          marcas y tipos (con palo, sin palo, compuesta).
        </li>
        <li>
          <strong>Limpieza de la Bombilla:</strong> Limpia tu bombilla
          regularmente para evitar obstrucciones y asegurar un buen flujo.
        </li>
      </ul>

